var firstSummand = Number(prompt("Введите первое слагаемое")); // ввод с клавиатуры первого слагаемого
var secondSummand = Number(prompt("Введите второе слагаемое")); // ввод с клавиатуры второго слагаемого
var sum = firstSummand + secondSummand; // формула нахождения суммы
alert("Результат сложения: " + sum); // вывод результата сложения